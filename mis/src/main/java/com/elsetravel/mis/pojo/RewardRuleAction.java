package com.elsetravel.mis.pojo;

public class RewardRuleAction {
	private String code;
	private String name;
	
	public RewardRuleAction(String code,String name){
		this.code = code;
		this.name = name;
	}

	public String getCode() {
		return code;
	}

	public String getName() {
		return name;
	}
	
	
}
